package com.ropii.desadigital.model;

public class ModelKonten {
    String id_konten_app, title_konten_app, subtitle_konten_app, konten_app, image_konten_app, lat, lng;

    public String getId_konten_app() {
        return id_konten_app;
    }

    public void setId_konten_app(String id_konten_app) {
        this.id_konten_app = id_konten_app;
    }

    public String getTitle_konten_app() {
        return title_konten_app;
    }

    public void setTitle_konten_app(String title_konten_app) {
        this.title_konten_app = title_konten_app;
    }

    public String getSubtitle_konten_app() {
        return subtitle_konten_app;
    }

    public void setSubtitle_konten_app(String subtitle_konten_app) {
        this.subtitle_konten_app = subtitle_konten_app;
    }

    public String getKonten_app() {
        return konten_app;
    }

    public void setKonten_app(String konten_app) {
        this.konten_app = konten_app;
    }

    public String getImage_konten_app() {
        return image_konten_app;
    }

    public void setImage_konten_app(String image_konten_app) {
        this.image_konten_app = image_konten_app;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }
}
